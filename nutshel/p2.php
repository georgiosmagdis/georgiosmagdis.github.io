								<p>More than 93% of the mass in our body, and to a greater extent of everything we see on Earth, is stardust produced inside the cores of previous generations of stars; hydrogen molecular clouds collapse to form stars that through nuclear fusion convert the primordial hydrogen to heavier elements. These elements subsequently enrich the interstellar medium of the galaxies through super nova explosions and stellar winds. This process of star formation started soon after the Big Bang and continues to take place in the present day. <p>

								<p>Today galaxies form stars at a rather leisurely pace. For example our Galaxy, and the majority of star forming galaxies in the local Universe, undergo a moderate star formation of approximately 2-3 stars per year. These “normal” galaxies are characterized by long lasting and smooth star formation episodes, converting gas into stars in a steady, gentle fashion. However, occasional interactions between massive galaxies, or mergers, trigger violent and short lived starburst events, that enhance the efficiency of the galaxies to convert gas into stars by a factor of 100. 
								Such events though are known to be extremely rare in the current cosmic epoch.<p>

								<p>But what about the past? Recent observations have revealed that our universe was about 50 times more active in the past and that the most of the stars in the universe were formed approximately ten billion years ago. There is also compelling evidence that the bulk of these stars were formed in massive galaxies that experienced high rates of star formation. Was the star-formation in these galaxies mainly driven by smooth cold gas accretion, similar to what we see in the majority of near-by star-forming galaxies, or rather by short-lived starburst events triggered by galaxy mergers? Is there a universal star-formation law that governs the efficiency at which galaxies form stars? What are the physical processes that shaped the evolution of the star formation activity in the galaxies over the last 10 billion years? <p>

								<p>Key answers to these questions are hidden in the interstellar medium of the galaxies, which is mainly made of gas and dust. The molecular gas, i.e. the material from which stars are formed, and the dust, which is the product of previous generations of stars, determine the evolution of a galaxy and reflect its current and past star-formation events. By exploiting new data and by developing new techniques, I pursue a concurrent study of the molecular gas and dust in distant star-forming galaxies. By measuring the gas mass reservoirs, the star formation rates, the morphology and the dynamical state of galaxies at various epochs of the universe I aim to ``paint'' the picture of star-formation activity through cosmic time and provide a coherent view of the processes that shaped galaxy evolution.<p>

